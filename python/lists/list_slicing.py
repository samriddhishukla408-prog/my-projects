numbers = [10, 20, 30, 40, 50, 60, 70] 
print("Full list:", numbers)
print("First 3 elements:", numbers[0:3])
print("Last 3 elements:", numbers[-3:])
print("Every 2nd element:", numbers[::2])
print("Reversed list:", numbers[::-1])
print("Element at index -1 (last):", numbers[-1])
print("Element at index -2 (second last):", numbers[-2])
print("Middle elements (index 2 to 4):", numbers[2:5])