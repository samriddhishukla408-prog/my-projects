string = "programming"
 
print("Original string:", string)
print("First 4 letters:", string[0:4])
print("Last 4 letters:", string[-4:])
print("Every 2nd letter:", string[::2])
print("Reversed string:", string[::-1])
print("Middle part (index 3 to 7):", string[3:8])
print("Single character at index 0:", string[0])
print("Single character at index -1:", string[-1])